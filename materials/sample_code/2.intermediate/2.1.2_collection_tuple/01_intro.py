# ### tuples are 
# ### ordered and 
# ### unchangeable/imutable
# ### allow duplicates

fruits = ("apple", "banana", "cherry")

# # data type
print(type(fruits))

# # indexing
print(fruits[0])

# # length: len() function
print(len(fruits))


# # the tuple() constructor
fruits = tuple(("apple", "banana", "cherry"))
print(type(fruits))
print(fruits)